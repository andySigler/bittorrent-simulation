var app = require('express')();
var server = require('http').createServer(app);

server.listen(8080);

///////////////////////////////////
///////////////////////////////////
///////////////////////////////////

app.get('/*', function (req, res) {
	if(req.url!=='/favicon.ico'){
		res.sendfile(__dirname+req.url);
	}
});

